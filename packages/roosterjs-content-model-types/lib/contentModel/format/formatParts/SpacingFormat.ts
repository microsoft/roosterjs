/**
 * Format of spacing
 */
export type SpacingFormat = {
    /**
     * Whether borders of cells are collapsed together
     */
    borderCollapse?: boolean;

    /**
     * Whether borders of cells are separated
     */
    borderSeparate?: boolean;

    /**
     * Cell padding value
     */
    cellPadding?: string;

    /**
     * Cell spacing value
     */
    cellSpacing?: string;
};
