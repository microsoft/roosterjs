/**
 * Format of margin
 */
export type MarginFormat = {
    /**
     * Margin top value
     */
    marginTop?: string;

    /**
     * Margin right value
     */
    marginRight?: string;

    /**
     * Margin bottom value
     */
    marginBottom?: string;

    /**
     * Margin left value
     */
    marginLeft?: string;

    /**
     * Margin-block start value
     */
    marginBlockStart?: string;

    /**
     * Margin-block end value
     */
    marginBlockEnd?: string;
};
