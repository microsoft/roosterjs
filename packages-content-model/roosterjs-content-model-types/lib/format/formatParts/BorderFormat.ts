/**
 * Format of border
 */
export type BorderFormat = {
    /**
     * Top border in format 'width style color'
     */
    borderTop?: string;

    /**
     * Right border in format 'width style color'
     */
    borderRight?: string;

    /**
     * Bottom border in format 'width style color'
     */
    borderBottom?: string;

    /**
     * Left border in format 'width style color'
     */
    borderLeft?: string;

    /**
     * Radius to be applied in all borders corners
     */
    borderRadius?: string;

    /**
     * Top border color
     */
    borderTopColor?: string;

    /**
     * Right border color
     */
    borderRightColor?: string;

    /**
     * Bottom border color
     */
    borderBottomColor?: string;

    /**
     * Left border color
     */
    borderLeftColor?: string;

    /**
     * Top border Style
     */
    borderTopStyle?: string;

    /**
     * Right border Style
     */
    borderRightStyle?: string;

    /**
     * Bottom border Style
     */
    borderBottomStyle?: string;

    /**
     * Left border Style
     */
    borderLeftStyle?: string;

    /**
     * Top border Width
     */
    borderTopWidth?: string;

    /**
     * Right border Width
     */
    borderRightWidth?: string;

    /**
     * Bottom border Width
     */
    borderBottomWidth?: string;

    /**
     * Left border Width
     */
    borderLeftWidth?: string;
};
